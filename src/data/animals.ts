import type { Animal, Position } from '@/types';

const generateTrajectory = (center: Position, points: number = 20): Position[] => {
  const trajectory: Position[] = [];
  let [x, y, z] = center;
  for (let i = 0; i < points; i++) {
    x += (Math.random() - 0.5) * 3;
    z += (Math.random() - 0.5) * 3;
    y = 0.5 + Math.sin(i * 0.3) * 0.2;
    trajectory.push([x, y, z]);
  }
  return trajectory;
};

